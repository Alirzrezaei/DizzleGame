package logic;

/**
 * This method is used during loading the data of the json file into LevelReader
 * object and store position and points of the symbol from json file. Gson
 * library is used to load and validate the types.
 *
 * @author Alireza
 */
public class boardObjects {

    private int points;
    private Position positions[];

    /**
     * This method is setting point for this object.
     *
     * @param points int point of object
     */
    public void setPoints(int points) {
        this.points = points;
    }

    /**
     * This method is setting
     *
     * @param positions
     */
    public void setPositions(Position[] positions) {
        this.positions = positions;
    }

    /**
     * returning the point of this object
     *
     * @return int point
     */
    public int getPoints() {
        return points;
    }

    /**
     * returning the position of this object(symbol)
     *
     * @return Position of symbol
     */
    public Position[] getPositions() {
        return positions;
    }
}
